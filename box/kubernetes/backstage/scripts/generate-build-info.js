// Auto-generates packages/app/src/buildInfo.ts with build metadata.
// Values: backstage version from backstage.json, git commit hash, build date,
// and installed plugin list with versions from workspace package.json files.
// Uses execFileSync to avoid shell injection.

const { execFileSync } = require('child_process');
const { readFileSync, writeFileSync, readdirSync } = require('fs');
const { resolve } = require('path');

const rootDir = resolve(__dirname, '..');
const outputPath = resolve(rootDir, 'packages/app/src/buildInfo.ts');

// 1. Backstage base version
const backstageJson = JSON.parse(
  readFileSync(resolve(rootDir, 'backstage.json'), 'utf8'),
);
const backstageVersion = backstageJson.version;

// 2. Commit SHA: git rev-parse, fallback to COMMIT_SHA env var
let commitSha = process.env.COMMIT_SHA || 'unknown';
try {
  commitSha = execFileSync('git', ['rev-parse', '--short', 'HEAD'], {
    cwd: rootDir,
    encoding: 'utf8',
  }).trim();
} catch {
  // git not available (e.g. Docker build without .git)
}

// 3. Build date
const buildDate = new Date().toISOString();

// 4. Installed plugins with versions from workspace package.json files
const pluginPattern = /^(@backstage\/plugin-|@backstage-community\/plugin-|@internal\/plugin-)/;
const pluginMap = new Map();

// Build a lookup for @internal plugin versions from plugins/*/package.json
const internalVersions = new Map();
try {
  const pluginDirs = readdirSync(resolve(rootDir, 'plugins'), {
    withFileTypes: true,
  });
  for (const dir of pluginDirs) {
    if (!dir.isDirectory()) continue;
    try {
      const pkg = JSON.parse(
        readFileSync(resolve(rootDir, 'plugins', dir.name, 'package.json'), 'utf8'),
      );
      if (pkg.name && pkg.version) {
        internalVersions.set(pkg.name, pkg.version);
      }
    } catch {
      // skip
    }
  }
} catch {
  // plugins directory not found
}

for (const ws of ['packages/app', 'packages/backend']) {
  const pkgPath = resolve(rootDir, ws, 'package.json');
  try {
    const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
    for (const deps of [pkg.dependencies, pkg.devDependencies]) {
      if (!deps) continue;
      for (const [name, declaredVersion] of Object.entries(deps)) {
        if (!pluginPattern.test(name)) continue;
        if (pluginMap.has(name)) continue;

        // Resolve workspace:* to actual version from plugin package.json
        let version = declaredVersion;
        if (typeof version === 'string' && version.startsWith('workspace:')) {
          version = internalVersions.get(name) || version;
        }
        pluginMap.set(name, version);
      }
    }
  } catch {
    // workspace package.json not found
  }
}

// 5. BUI migration stats: scan .tsx files for @backstage/ui vs @material-ui imports
const { readdirSync: readdir2, statSync } = require('fs');
const { join } = require('path');

function collectTsxFiles(dir) {
  const results = [];
  try {
    for (const entry of readdir2(dir, { withFileTypes: true })) {
      const full = join(dir, entry.name);
      if (entry.isDirectory() && entry.name !== 'node_modules' && entry.name !== 'dist') {
        results.push(...collectTsxFiles(full));
      } else if (entry.isFile() && entry.name.endsWith('.tsx')) {
        results.push(full);
      }
    }
  } catch { /* skip */ }
  return results;
}

const scanDirs = [
  resolve(rootDir, 'packages/app/src'),
  resolve(rootDir, 'plugins'),
];

let buiOnly = 0;
let muiOnly = 0;
let mixed = 0;
const buiFiles = [];
const muiFiles = [];
const mixedFiles = [];

for (const dir of scanDirs) {
  for (const file of collectTsxFiles(dir)) {
    const src = readFileSync(file, 'utf8');
    const hasBui = /from\s+['"]@backstage\/ui['"]/.test(src);
    const hasMui = /from\s+['"]@material-ui\//.test(src);
    const rel = file.replace(rootDir + '/', '');
    if (hasBui && !hasMui) { buiOnly++; buiFiles.push(rel); }
    else if (!hasBui && hasMui) { muiOnly++; muiFiles.push(rel); }
    else if (hasBui && hasMui) { mixed++; mixedFiles.push(rel); }
  }
}

const totalUiFiles = buiOnly + muiOnly + mixed;
const buiPercent = totalUiFiles > 0 ? Math.round((buiOnly / totalUiFiles) * 100) : 0;

const buiMigration = { buiOnly, muiOnly, mixed, total: totalUiFiles, percent: buiPercent };

const plugins = Array.from(pluginMap.entries())
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([name, version]) => ({ name, version }));

const pluginsLiteral = plugins
  .map(p => `    { name: '${p.name}', version: '${p.version}' },`)
  .join('\n');

const content = `// Auto-generated by scripts/generate-build-info.js
export const BUILD_INFO: {
  backstageVersion: string;
  commitSha: string;
  buildDate: string;
  plugins: { name: string; version: string }[];
  buiMigration: {
    buiOnly: number;
    muiOnly: number;
    mixed: number;
    total: number;
    percent: number;
    files: { bui: string[]; mui: string[]; mixed: string[] };
  };
} = {
  backstageVersion: '${backstageVersion}',
  commitSha: '${commitSha}',
  buildDate: '${buildDate}',
  plugins: [
${pluginsLiteral}
  ],
  buiMigration: {
    buiOnly: ${buiMigration.buiOnly},
    muiOnly: ${buiMigration.muiOnly},
    mixed: ${buiMigration.mixed},
    total: ${buiMigration.total},
    percent: ${buiMigration.percent},
    files: {
      bui: ${JSON.stringify(buiFiles)},
      mui: ${JSON.stringify(muiFiles)},
      mixed: ${JSON.stringify(mixedFiles)},
    },
  },
};
`;

writeFileSync(outputPath, content, 'utf8');
console.log(
  `buildInfo.ts generated: v${backstageVersion} / ${commitSha} / ${buildDate} / ${plugins.length} plugins`,
);
